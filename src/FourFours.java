public class FourFours {
    public static void main(String[] args) {
        System.out.println("Zero is " +(4+4-4-4));
        System.out.println("One is " +(4/4+4-4));
        System.out.println("Two is " +(4/4+4/4));
        System.out.println("Three is " +((4+4+4)/4));
        System.out.println("Four is " +(4+(4-4)*4));
        System.out.println("Five is " +((4*4+4)/4));
        System.out.println("Six is " +((4+4)/4+4));
        System.out.println("Seven is " +(4+4-(4/4)));
        System.out.println("Eight is " +(4+4+4-4));
        System.out.println("Nine is " +(4/4+4+4));
    }
}